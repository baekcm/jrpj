package com.jnet.domain;

import org.apache.commons.lang.builder.ToStringBuilder;
import org.apache.commons.lang.builder.ToStringStyle;

public class Locker extends Member {
	private static final long serialVersionUID = 1L;	
	private String boxNo;
	private String itemNm;
	private String startDt;
	private String lastEndDt;
	private String rcvAmt;
	private String issueDt;
	private String upId;
	
	
	public String getUpId() {
		return upId;
	}
	public void setUpId(String upId) {
		this.upId = upId;
	}
	public String getBoxNo() {
		return boxNo;
	}
	public void setBoxNo(String boxNo) {
		this.boxNo = boxNo;
	}
	public String getItemNm() {
		return itemNm;
	}
	public void setItemNm(String itemNm) {
		this.itemNm = itemNm;
	}
	public String getStartDt() {
		return startDt;
	}
	public void setStartDt(String startDt) {
		this.startDt = startDt;
	}
	public String getLastEndDt() {
		return lastEndDt;
	}
	public void setLastEndDt(String lastEndDt) {
		this.lastEndDt = lastEndDt;
	}
	public String getRcvAmt() {
		return rcvAmt;
	}
	public void setRcvAmt(String rcvAmt) {
		this.rcvAmt = rcvAmt;
	}
	public String getIssueDt() {
		return issueDt;
	}
	public void setIssueDt(String issueDt) {
		this.issueDt = issueDt;
	}
	public String toString() {
		return ToStringBuilder.reflectionToString(this, ToStringStyle.MULTI_LINE_STYLE);
	}
}
